-- =============================================================================
-- AIOS CLINICS — Schema completo (padrão multi-clínica, naming universal)
-- =============================================================================
-- Como usar:
--   1. Crie um novo projeto Supabase para a clínica.
--   2. Abra o SQL Editor (ou rode via MCP `apply_migration`).
--   3. Cole este arquivo inteiro e execute. Idempotente — pode rodar 2x.
--   4. Pegue Project URL + publishable key e configure no app
--      (ver docs/CLONE_GUIDE.md).
--
-- Convenções:
--   - Prefixo `aiosclinics_` em todas as tabelas (multi-clínica friendly).
--   - RLS aberta (anon role) — todo acesso é via publishable key no client.
--     Quando adicionar auth, restrinja as policies por user_id/role.
--   - IDs bigint identity para tabelas operacionais (agenda/anamnese/acomp),
--     uuid para catálogos (responsaveis/procedimentos).
-- =============================================================================


-- =============================================================================
-- 1) AGENDAMENTOS
-- =============================================================================
create table if not exists public.aiosclinics_agendamento (
  id                       bigint generated by default as identity primary key,
  created_at               timestamptz not null default now(),
  "Data"                   text,           -- formato BR: "dd/MM/yyyy HH:mm"
  "Nome"                   text,
  "Número"                 text,
  "Anotações"              text,
  "Confirmação"            text,
  "1 Dia antes"            text,
  "No dia"                 text,
  "Valor"                  numeric,
  "Responsavel_Agendamento" text,
  "Responsavel_Atendimento" text,
  "Tipo"                   text,
  "Procedimento"           text
);

alter table public.aiosclinics_agendamento enable row level security;
create policy "public read agendamento"   on public.aiosclinics_agendamento for select using (true);
create policy "public insert agendamento" on public.aiosclinics_agendamento for insert with check (true);
create policy "public update agendamento" on public.aiosclinics_agendamento for update using (true);
create policy "public delete agendamento" on public.aiosclinics_agendamento for delete using (true);


-- =============================================================================
-- 2) ANAMNESE
