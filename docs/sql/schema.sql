-- =============================================================================
-- 7) STORAGE BUCKET — fotos do acompanhamento
-- =============================================================================
insert into storage.buckets (id, name, public)
values ('aiosclinics-acompanhamento-fotos', 'aiosclinics-acompanhamento-fotos', true)
on conflict (id) do nothing;

-- Policies do bucket (executam contra storage.objects)
do $$
begin
  if not exists (select 1 from pg_policies where schemaname='storage' and tablename='objects' and policyname='public read aios fotos') then
    create policy "public read aios fotos"   on storage.objects for select using (bucket_id = 'aiosclinics-acompanhamento-fotos');
  end if;
  if not exists (select 1 from pg_policies where schemaname='storage' and tablename='objects' and policyname='public upload aios fotos') then
    create policy "public upload aios fotos" on storage.objects for insert with check (bucket_id = 'aiosclinics-acompanhamento-fotos');
  end if;
  if not exists (select 1 from pg_policies where schemaname='storage' and tablename='objects' and policyname='public update aios fotos') then
    create policy "public update aios fotos" on storage.objects for update using (bucket_id = 'aiosclinics-acompanhamento-fotos');
  end if;
  if not exists (select 1 from pg_policies where schemaname='storage' and tablename='objects' and policyname='public delete aios fotos') then
    create policy "public delete aios fotos" on storage.objects for delete using (bucket_id = 'aiosclinics-acompanhamento-fotos');
  end if;
end$$;


