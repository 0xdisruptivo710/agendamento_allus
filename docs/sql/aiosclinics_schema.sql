-- ============================================================
-- 5) RESPONSÁVEIS (agendamento / atendimento)
-- ============================================================
create table if not exists public.aiosclinics_responsaveis (
  id uuid primary key default gen_random_uuid(),
  created_at timestamptz not null default now(),
  nome text not null,
  tipo text not null check (tipo in ('agendamento', 'atendimento')),
  ativo boolean default true,
  unique (nome, tipo)
);

alter table public.aiosclinics_responsaveis enable row level security;
create policy "public read responsaveis"   on public.aiosclinics_responsaveis for select using (true);
create policy "public insert responsaveis" on public.aiosclinics_responsaveis for insert with check (true);
create policy "public update responsaveis" on public.aiosclinics_responsaveis for update using (true);
create policy "public delete responsaveis" on public.aiosclinics_responsaveis for delete using (true);


