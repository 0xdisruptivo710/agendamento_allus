-- ============================================================
-- 1) AGENDAMENTOS
-- ============================================================
create table if not exists public.aiosclinics_agendamento (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  "Data" text,                         -- formato BR: dd/MM/yyyy HH:mm
  "Nome" text,
  "Número" text,
  "Anotações" text,
  "Confirmação" text,
  "1 Dia antes" text,
  "No dia" text,
  "Valor" numeric,
  "Responsavel_Agendamento" text,
  "Responsavel_Atendimento" text,
  "Tipo" text,
  "Procedimento" text
);

alter table public.aiosclinics_agendamento enable row level security;
create policy "public read agendamento"   on public.aiosclinics_agendamento for select using (true);
create policy "public insert agendamento" on public.aiosclinics_agendamento for insert with check (true);
create policy "public update agendamento" on public.aiosclinics_agendamento for update using (true);
create policy "public delete agendamento" on public.aiosclinics_agendamento for delete using (true);


-- ============================================================
-- 2) ANAMNESE
-- ============================================================
create table if not exists public.aiosclinics_anamnese (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  agendamento_id bigint references public.aiosclinics_agendamento(id) on delete set null,
  paciente_nome text,
  paciente_telefone text,
  data_anamnese date default current_date,
  queixa_principal text,
  historico_saude text,
  alergias text,
  medicamentos text,
  cirurgias_previas text,
  gestante boolean default false,
  procedimentos_anteriores text,
  tabagismo boolean default false,
  etilismo boolean default false,
  tipo_pele text,
  exposicao_solar text,
  observacoes text
);

alter table public.aiosclinics_anamnese enable row level security;
create policy "public read anamnese"   on public.aiosclinics_anamnese for select using (true);
create policy "public insert anamnese" on public.aiosclinics_anamnese for insert with check (true);
create policy "public update anamnese" on public.aiosclinics_anamnese for update using (true);
create policy "public delete anamnese" on public.aiosclinics_anamnese for delete using (true);


-- ============================================================
-- 3) ACOMPANHAMENTO
-- ============================================================
create table if not exists public.aiosclinics_acompanhamento (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  agendamento_id bigint references public.aiosclinics_agendamento(id) on delete set null,
  paciente_nome text,
  paciente_telefone text,
  data_inicio date default current_date,
  data_fim_tratamento date,
  status text default 'em_andamento',     -- em_andamento | finalizado | enviado
  observacoes text
);

alter table public.aiosclinics_acompanhamento enable row level security;
create policy "public read acomp"   on public.aiosclinics_acompanhamento for select using (true);
create policy "public insert acomp" on public.aiosclinics_acompanhamento for insert with check (true);
create policy "public update acomp" on public.aiosclinics_acompanhamento for update using (true);
create policy "public delete acomp" on public.aiosclinics_acompanhamento for delete using (true);


-- ============================================================
-- 4) FOTOS DO ACOMPANHAMENTO
-- ============================================================
create table if not exists public.aiosclinics_acompanhamento_fotos (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  acompanhamento_id bigint not null references public.aiosclinics_acompanhamento(id) on delete cascade,
  foto_url text not null,
  foto_path text,
  data_foto date default current_date,
  ordem int default 0,
  nota text
